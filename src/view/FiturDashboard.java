    private void tampilDashboard(){
        try {
            String queryP = "SELECT COUNT(*) AS total_produk FROM produk";
            PreparedStatement psp = conn.prepareStatement(queryP);
            ResultSet rsp = psp.executeQuery();

            if (rsp.next()) {
                int totalProduk = rsp.getInt("total_produk");
                total_produk.setText(String.valueOf(totalProduk)); 
            }
            
            String queryT = "SELECT COUNT(*) AS total_trx FROM transaksi";
            PreparedStatement pst = conn.prepareStatement(queryT);
            ResultSet rst = pst.executeQuery();
            
            if(rst.next()){
                int totalTrans = rst.getInt("total_trx");
                total_trans.setText(String.valueOf(totalTrans));
            }

        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("Error saat menampilkan dashboard: " + e.getMessage());
        }
    }
    

